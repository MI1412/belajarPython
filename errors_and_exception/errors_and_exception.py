#**Ringkasan tentang Errors dan Exceptions:**

#1. **Error dan Pesan Kesalahan:**
#   - Sejauh ini, pesan kesalahan hanya disebutkan secara singkat, tetapi jika Anda mencoba contoh-contoh dalam Python, Anda mungkin sudah melihat beberapa di antaranya. 
#   - Ada dua jenis kesalahan utama dalam Python, yaitu **Syntax Errors** dan **Exceptions**.

#2. **Syntax Errors:**
#   - **Syntax errors** atau kesalahan sintaks adalah kesalahan yang terjadi ketika Python tidak dapat memahami kode yang ditulis. Kesalahan ini biasanya disebabkan oleh penulisan kode yang tidak sesuai dengan aturan sintaks Python, seperti melewatkan tanda kurung atau salah penempatan kata kunci.
#   - Ketika terjadi kesalahan sintaks, Python akan memberikan pesan kesalahan yang menjelaskan letak kesalahan dan apa yang salah di kode tersebut.

#3. **Exceptions:**
#   - **Exceptions** adalah jenis kesalahan yang terjadi selama eksekusi program, meskipun sintaks dari kode tersebut benar. Exception muncul ketika Python menghadapi situasi yang tidak dapat diproses, seperti mencoba membagi angka dengan nol atau mengakses variabel yang tidak ada.
#   - Berbeda dengan syntax errors, **exceptions** bisa ditangani dalam program untuk mencegah program berhenti berjalan. 

#Kesimpulannya, **syntax errors** terjadi ketika kode tidak sesuai dengan aturan penulisan Python, sedangkan **exceptions** adalah kesalahan yang terjadi selama program berjalan meskipun kode secara sintaks benar.